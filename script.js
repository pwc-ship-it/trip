function assignWtLanes(wts){
  // wts: [{start,end,...}] мқјеӘӣ?wtмқјlane иё°лҚҠмғҮ йҒәВҖмқј  var lanes=[]; // lanes[i] = п§ҚлҚү?п§Қлҗұм‘қжҝЎмқјм•ёк¶ғ лӮ м§ң
  wts.forEach(function(wt){
    var assigned=false;
    for(var i=0;i<lanes.length;i++){
      if(wt.start>lanes[i]){ // мқј?лҚүм”Өмқј?г…јлјұеӘӣмқјмқјлҚүм“¬
        wt._lane=i;lanes[i]=wt.end;assigned=true;break;
      }
    }
    if(!assigned){wt._lane=lanes.length;lanes.push(wt.end);}
  });
  return lanes.length;
}

